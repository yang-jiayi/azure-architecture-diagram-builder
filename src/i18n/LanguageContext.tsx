function translateJapanese(key: StaticTranslationKey): string {
  const exact = exactJapanese[key];
  if (exact !== undefined) return exact;
  let result: string = key;
  for (const [english, japanese] of phraseJapanese) {
    if (result.includes(english)) result = result.split(english).join(japanese);
  }
  return result;
}

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (match, name: string) =>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match,
  );
}

interface LanguageContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  toggleLanguage: () => void;
  t: (key: TranslationKey, params?: TranslationParams) => string;
  translate: (text: string, params?: TranslationParams) => string;
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

function getInitialLanguage(): Language {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'ja') return stored;
  } catch {
    // localStorage can be unavailable in privacy-restricted contexts.
  }
  return navigator.language.toLowerCase().startsWith('ja') ? 'ja' : 'en';
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(getInitialLanguage);

  const setLanguage = useCallback((next: Language) => {
    setLanguageState(next);
    try { localStorage.setItem(STORAGE_KEY, next); } catch { /* ignore */ }
  }, []);

  const toggleLanguage = useCallback(() => {
    setLanguage(language === 'en' ? 'ja' : 'en');
  }, [language, setLanguage]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback((key: TranslationKey, params?: TranslationParams) => {
    const template = language === 'en'
      ? (key in extraEnglish ? extraEnglish[key as ExtraTranslationKey] : key)
      : (key in extraJapanese ? extraJapanese[key as ExtraTranslationKey] : translateJapanese(key as StaticTranslationKey));
    return interpolate(template, params);
  }, [language]);

  const translate = useCallback((text: string, params?: TranslationParams) => {
    return interpolate(language === 'en' ? text : translateJapanese(text as StaticTranslationKey), params);
  }, [language]);

  const value = useMemo(
    () => ({ language, setLanguage, toggleLanguage, t, translate }),
    [language, setLanguage, toggleLanguage, t, translate],
  );
  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  const value = useContext(LanguageContext);
  if (!value) throw new Error('useLanguage must be used within LanguageProvider');
  return value;
}
